#!/bin/bash
# סקריפט הרצה לבדיקת שלב 2 - Linux/Mac

echo "========================================"
echo "בדיקת שלב 2: חיבור ליומן וזמינות"
echo "========================================"
echo ""

# בדוק אם Python מותקן
if ! command -v python3 &> /dev/null; then
    echo "שגיאה: Python3 לא מותקן"
    exit 1
fi

# הרץ את סקריפט הבדיקה
echo "מריץ בדיקות..."
echo ""
python3 database/check_stage2.py

if [ $? -eq 0 ]; then
    echo ""
    echo "========================================"
    echo "כל הבדיקות עברו בהצלחה!"
    echo "========================================"
    exit 0
else
    echo ""
    echo "========================================"
    echo "יש בעיות שצריך לתקן!"
    echo "========================================"
    exit 1
fi

